package Byte;

public class bytePractical {

    public static void main(String[] args) {
        // Declare a byte variable
        byte myByte = 100;

        // Print the value of the byte variable
        System.out.println("Value of myByte: " + myByte);

        // Perform arithmetic operation with bytes
        byte a = 50;
        byte b = 70;
        byte sum = (byte) (a + b);
        System.out.println("Sum of a and b: " + sum);
    }

}
